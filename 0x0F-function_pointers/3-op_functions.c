/**
 * op_add - Adds two int numbers
 * @a: argument passed
 * @b: argument passed
 * Return: result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two int numbers
 * @a: argument passed
 * @b: argument passed
 * Return: result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplies two int numbers
 * @a: argument passed
 * @b: argument passed
 * Return: result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divides two int numbers
 * @a: argument passed
 * @b: argument passed
 * Return: result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Modules two int numbers
 * @a: argument passed
 * @b: argument passed
 * Return: result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
