/**
 * op_add - Adding two int numbers
 * @a: value one
 * @b: value two
 * Return: sum
 */

int op_add(int a, int b)
{
	int sum;

	sum = a + b;
	return (sum);
}

/**
 * op_sub - subtracting two int numbers
 * @a: value one
 * @b: value two
 * Return: difference
 */

int op_sub(int a, int b)
{
	int diff;

	diff = a - b;
	return (diff);
}

/**
 * op_mul - Multiply two int numbers
 * @a: value one
 * @b: value two
 * Return: product
 */

int op_mul(int a, int b)
{
	int prod;

	prod = a * b;
	return (prod);
}

/**
 * op_div - Divide two int numbers
 * @a: value one
 * @b: value two
 * Return: product
 */

int op_div(int a, int b)
{
	int div;

	div = a / b;
	return (div);
}

/**
 * op_mod - Modules two int numbers
 * @a: value one
 * @b: value two
 * Return: product
 */

int op_mod(int a, int b)
{
	int x;

	x = a % b;
	return (x);
}
